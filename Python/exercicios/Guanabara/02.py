'''
Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

'''

n = int(input('Digite um número: '))
n1 = n * 2 # multiplicar
n2 = n * 3
n3 = n **(1/2) # raiz quadrada

print(f'O dobro e o triplo de {n} são {n1} e {n2}\nE sua raiz quadrada é {n3}')

